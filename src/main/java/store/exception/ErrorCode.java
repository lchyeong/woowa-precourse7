package store.exception;

public enum ErrorCode {
    CODE("E001", "[ERRE] ㅎㅇ");

    private final String code;
    private final String message;

    ErrorCode(String code, String message) {
        this.code = code;
        this.message = message;
    }

    public String getMessage() {
        return message;
    }

    public String getCode() {
        return code;
    }
}
